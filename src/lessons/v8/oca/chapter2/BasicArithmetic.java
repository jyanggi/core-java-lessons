package lessons.v8.oca.chapter2;

public class BasicArithmetic {

	
	public static void main(String [] args) {
		
		System.out.printf("5 + 3: %d\n", 5+3);
		System.out.printf("5 - 3: %d\n",5-3);
		System.out.printf("5 * 3: %d\n",5*3);
		System.out.printf("5 / 3: %d\n",5/3);
		System.out.printf("5 %% 3: %d\n",5%3);
		System.out.printf("5 / 3.0: %f\n",5/3.0);

	}
}
